function openPage() {
    var x = document.getElementById("search").value;

    if (x === "Seriale", "seriale") {
        window.open("../Project/Seriale.html");
    }

    if (x === "Home", "home") {
        window.open("http://127.0.0.1:5500/index.html");
    }

    if (x === "Anime", "anime") {
        window.open("http://127.0.0.1:5500/Anime.html");
    }

}